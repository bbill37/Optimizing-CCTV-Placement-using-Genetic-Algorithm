import random

def gap_random_expovar(low, high, gap, scale):
    while low < high:
        low += random.expovariate(1/scale)
        if low <= high:
            yield low
            low += gap

for _ in range(10):
    print(list(gap_random_expovar(low=0, high=1000, gap=20, scale=5))) 
